export function trackToPlayerFormat(track) {
  return {
    id: track.id,
    name: track.title ?? track.name,
    artist: track.artist,
    album: track.album,
    cover: track.cover ?? track.images?.[0]?.url,
    duration: track.duration ?? track.duration_formatted,
    currentTime: "0:00",
    preview_url: track.preview_url,
    uri: track.uri,
    external_urls: track.external_urls,
  };
}

export function spotifyTrackToPlayerFormat(track) {
  return trackToPlayerFormat({
    id: track.id,
    name: track.name,
    artist: track.artist ?? track.artists?.[0],
    album: track.album,
    images: track.images,
    duration_formatted: track.duration_formatted,
    duration_ms: track.duration_ms,
    preview_url: track.preview_url,
    uri: track.uri,
    external_urls: track.external_urls,
  });
}
